# 4. PROFILE_UPDATE_PROMPT — обновление семантического профиля
# Модель: GPT-4o-mini | Max output: 500 tok | response_format: json_object
# ---------------------------------------------------------------------------

PROFILE_UPDATE_PROMPT = """Ты обновляешь профиль пользовательницы AI-компаньона. На входе: текущий профиль, новые сообщения и конспект разговора.

Твоя задача: найти НОВЫЕ ФАКТЫ о пользовательнице и вернуть diff (изменения).

ТЕКУЩИЙ ПРОФИЛЬ:
{current_profile}

НОВЫЕ СООБЩЕНИЯ:
{new_messages}

КОНСПЕКТ РАЗГОВОРА:
{episode_summary}

БУФЕР ФАКТОВ (из мини-обновлений):
{pending_facts}

Ответь СТРОГО в JSON:
{{
  "set_fields": {{
    "поле": "значение"
  }},
  "add_to_lists": {{
    "people": [{{"name": "имя", "relation": "роль", "description": "описание", "how_user_calls": "как обращается"}}],
    "triggers": ["новый триггер"],
    "strengths": ["новая сильная сторона"],
    "achievements": ["новое достижение"],
    "sensitive_topics": ["новая чувствительная тема"]
  }},
  "remove_fields": []
}}

ПРАВИЛА (ВАЖНО):
1. MERGE, не перезапись. Добавляй к существующим данным, не заменяй
2. Уровни уверенности:
   - Прямое высказывание ("мне 32 года") → добавляй
   - Из контекста ("работаю в офисе") → добавляй
   - Двусмысленно ("наверное я интроверт") → ПРОПУСТИ
3. Эмоции ≠ факты: "мне грустно" → НЕ в профиль. "Мне 32 года" → в профиль
4. Карта людей:
   - Человек добавляется или обновляется, но НИКОГДА не удаляется
   - Мама/папа → how_user_calls всегда "мама"/"папа" (НЕ по имени)
5. Если новых фактов нет → верни {{"set_fields": {{}}, "add_to_lists": {{}}, "remove_fields": []}}
6. remove_fields всегда []  (мы НИКОГДА не удаляем данные из профиля)

СТОП-ЛИСТ (НЕ добавлять в обычные поля профиля):
- Текущее настроение / эмоциональное состояние → это для emotion_log
- Временные события ("сегодня дождь") → не факт о человеке
- Мнения AI-компаньона → только факты ПОЛЬЗОВАТЕЛЬНИЦЫ

ЧУВСТВИТЕЛЬНЫЕ ТЕМЫ:
- Травмы, насилие, зависимости, болезни, утраты, аборты → добавлять ТОЛЬКО в поле sensitive_topics
- Никогда не добавлять чувствительные темы в main_problem, description людей и другие обычные поля
- Уровень уверенности: только ПРЯМОЕ высказывание ("у меня был выкидыш"). Из контекста — НЕ добавлять
- Пример: "муж пьёт" → sensitive_topics: ["алкоголизм мужа"]. НЕ в people[муж].description"""


