def get_all_questions_text() -> str:
    lines = ["БИБЛИОТЕКА МОЩНЫХ ВОПРОСОВ:\n"]
    category_names = {
        "fear": "Выявление страхов",
        "cost_of_inaction": "Цена бездействия",
        "pattern_confrontation": "Конфронтация с паттерном",
        "specificity": "Конкретизация действий",
        "values": "Ценности и смыслы",
        "identity": "Работа с идентичностью",
        "excuses": "Работа с отговорками",
    }
    for key, name in category_names.items():
        lines.append(f"\n{name}:")
        for q in QUESTIONS_LIBRARY[key]:
            lines.append(f"  • {q}")
    return "\n".join(lines)
